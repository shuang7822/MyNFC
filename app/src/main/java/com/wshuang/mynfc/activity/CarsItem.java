package com.wshuang.mynfc.activity;

public class CarsItem {
    private String CarsDate;
    private String CarsFltNr;
    private String CarsTotal;

    public String getCarsDate() {
        return CarsDate;
    }

    public void setCarsDate(String carsDate) {
        CarsDate = carsDate;
    }

    public String getCarsFltNr() {
        return CarsFltNr;
    }

    public void setCarsFltNr(String carsFltNr) {
        CarsFltNr = carsFltNr;
    }

    public String getCarsTotal() {
        return CarsTotal;
    }

    public void setCarsTotal(String carsTotal) {
        CarsTotal = carsTotal;
    }

}
